function pricingForecastScenarioReducer(state = initialState, action) {
  switch (action.type) {
    case DEFAULT_ACTION:
      return state;
    case FORECAST_SCENARIO_DATA_SUCCESS:
      return state.set('chartData', action.data);
    case FORECAST_EVENT_DATA_SUCCESS:
      console.log(FORECAST_EVENT_DATA_SUCCESS);
      return state.set('forecastEventData', action.data[0]);
    case UPDATE_EVENT_ID:
      return state.set('eventId', action.data);
    case UPDATE_SCENARIO_ID:
      return state.set('scenarioId', action.data);
    case UPDATE_SCENARIO_DATA_SUCCESS:
      return state.set('scenarioData', action.data);
    case GENERATE_PRICE_GRAVITY_CHART_SUCCESS:
      return state.set('priceGravityChart', action.data);
    default:
      return state;
  }
}

